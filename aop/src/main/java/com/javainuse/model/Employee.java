package com.javainuse.model;

public class Employee {
	private String CEP;
	private String rua;

	public Employee() {
	}

	public String getRua() {
		return rua;
	}

	public void setRua(String rua) {
		this.rua = rua;
	}

	public String getCep() {
		return CEP;
	}

	public void setCep(String CEP) {
		this.CEP = CEP;
	}

}